    public boolean isAnyFieldsEmpty() {
        if (nameField.getText().equals("") || codeField.getText().equals("")) {
            return true;

        }
        return false;
    }

    public void setFields(Product product) {
        nameField.setText(product.getName());
        codeField.setText(product.getCode());
    }

    public Product getProduct() {
        String name = nameField.getText();
        String code = codeField.getText();
        Product product = new Product(0, name, code, 0, 0);
        return product;
    }

    public void clearFields() {
        nameField.setText("");
        codeField.setText("");
    }

    public void fillTable() {
        defaultTableModel = new DefaultTableModel(columns, 0);
        List<Product> products = productDAO.getAllProducts();

        for (Product p : products) {
            Object row[] = {p.getProductId(), p.getName(), p.getCode()};
            defaultTableModel.addRow(row);
            productTable.setModel(defaultTableModel);
        }
        productTable.getColumnModel().getColumn(0).setWidth(0);
        productTable.getColumnModel().getColumn(0).setMinWidth(0);
        productTable.getColumnModel().getColumn(0).setMaxWidth(0);

    }

