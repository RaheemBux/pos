    public void fillCategoryCombo() {
        List<ExpenseCategory> expenseCategorys = expenseCategoryDAO.getAllExpenseCategories();
        for (ExpenseCategory expenseCategory : expenseCategorys) {
            categoryCombo.addItem(expenseCategory.getCategory());
        }
    }

    public boolean isAnyFieldsEmpty() {
        if (nameField.getText().equals("") || refNumberField.getText().equals("")
                || amountField.getText().equals("")) {
            return true;

        }
        return false;
    }

    public void setFields(Expense expense) {
        nameField.setText(expense.getName());
        refNumberField.setText(expense.getRefNumber());
        amountField.setText(String.valueOf(expense.getAmount()));
        categoryCombo.setSelectedItem(expense.getExpenseCategory().getCategory());
        expenseDateField.setDate(expense.getExpenseDate());

    }

    public Expense getExpense() {
        String name = nameField.getText();
        String refNumber = refNumberField.getText();
        double amount = Double.parseDouble(amountField.getText());
        String category = categoryCombo.getSelectedItem().toString();
        java.sql.Date expenseDate = new java.sql.Date(expenseDateField.getDate().getTime());
        ExpenseCategory expenseCategory = expenseCategoryDAO.getExpenseCategoryByCategory(category);
        Expense expense = new Expense(0, name, refNumber, amount, expenseDate, expenseCategory);
        return expense;
    }

    public void clearFields() {
        nameField.setText("");
        refNumberField.setText("");
        amountField.setText("");
        categoryCombo.setSelectedIndex(0);
        expenseDateField.setDate(null);
    }

    public void fillTable() {
        defaultTableModel = new DefaultTableModel(columns, 0);
        List<Expense> expenses = expenseDAO.getAllExpenses();

        for (Expense e : expenses) {
            Object row[] = {e.getExpenseId(), e.getExpenseCategory().getCategory(),
                e.getName(), e.getRefNumber(), e.getAmount(), e.getExpenseDate()};
            defaultTableModel.addRow(row);
            expenseTable.setModel(defaultTableModel);
        }
        expenseTable.getColumnModel().getColumn(0).setWidth(0);
        expenseTable.getColumnModel().getColumn(0).setMinWidth(0);
        expenseTable.getColumnModel().getColumn(0).setMaxWidth(0);

    }

